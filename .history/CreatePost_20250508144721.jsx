import React, { useState } from 'react';

const danceStyles = [
  'Hip Hop',
  'Ballet',
  'Salsa',
  'Jazz',
  'Contemporary',
  'Tap',
  'Ballroom',
];

export default function CreatePost() {
  const [caption, setCaption] = useState('');
  const [location, setLocation] = useState('');
  const [danceStyle, setDanceStyle] = useState('');
  const [image, setImage] = useState(null);

  const handleImageChange = (e) => {
    if (e.target.files && e.target.files[0]) {
      setImage(URL.createObjectURL(e.target.files[0]));
    }
  };

  return (
    <div style={{ maxWidth: 350, margin: '0 auto', padding: 24, fontFamily: 'sans-serif', background: '#fff', borderRadius: 12, boxShadow: '0 2px 8px #eee' }}>
      {/* Back Button */}
      <button style={{ background: 'none', border: 'none', fontSize: 24, cursor: 'pointer', marginBottom: 8 }}>&larr;</button>
      <h2 style={{ textAlign: 'center', margin: '8px 0' }}>Create Post</h2>
      <h3 style={{ textAlign: 'center', margin: '16px 0 8px' }}>Add Image</h3>
      {/* Image Upload */}
      <label style={{ display: 'flex', flexDirection: 'column', alignItems: 'center', cursor: 'pointer' }}>
        {image ? (
          <img src={image} alt="Preview" style={{ width: 120, height: 120, objectFit: 'cover', borderRadius: 12, marginBottom: 8 }} />
        ) : (
          <div style={{ width: 120, height: 120, background: '#b39ddb', borderRadius: 12, display: 'flex', alignItems: 'center', justifyContent: 'center', marginBottom: 8, position: 'relative' }}>
            <svg width="48" height="48" fill="#fff" viewBox="0 0 24 24"><path d="M21 19V5a2 2 0 0 0-2-2H5a2 2 0 0 0-2 2v14a2 2 0 0 0 2 2h14a2 2 0 0 0 2-2zm-2 0H5V5h14zm-7-2a3 3 0 1 1 0-6 3 3 0 0 1 0 6zm0-2a1 1 0 1 0 0-2 1 1 0 0 0 0 2z"/></svg>
            <span style={{ position: 'absolute', bottom: 8, right: 8, background: '#9575cd', borderRadius: '50%', width: 32, height: 32, display: 'flex', alignItems: 'center', justifyContent: 'center', color: '#fff', fontSize: 20, border: '2px solid #fff' }}>+</span>
          </div>
        )}
        <input type="file" accept="image/*" onChange={handleImageChange} style={{ display: 'none' }} />
      </label>
      {/* Filter */}
      <div style={{ display: 'flex', alignItems: 'center', margin: '16px 0 4px' }}>
        <span style={{ fontSize: 20, marginRight: 6 }}>‚öôÔ∏è</span>
        <span style={{ fontWeight: 600, fontSize: 18 }}>Filter</span>
      </div>
      {/* Caption */}
      <div style={{ marginBottom: 12 }}>
        <label style={{ fontWeight: 500, fontSize: 15 }}>Create Captions</label>
        <textarea
          placeholder="Type here..."
          value={caption}
          onChange={e => setCaption(e.target.value)}
          style={{ width: '100%', minHeight: 60, borderRadius: 8, border: '1px solid #b39ddb', padding: 8, marginTop: 4, resize: 'vertical' }}
        />
      </div>
      {/* Location */}
      <div style={{ marginBottom: 12, display: 'flex', alignItems: 'center', background: '#f3e5f5', borderRadius: 6 }}>
        <span style={{ fontSize: 18, margin: '0 8px' }}>üìç</span>
        <input
          type="text"
          placeholder="Add Location"
          value={location}
          onChange={e => setLocation(e.target.value)}
          style={{ border: 'none', background: 'transparent', outline: 'none', flex: 1, padding: 8, fontSize: 15 }}
        />
      </div>
      {/* Dance Style Dropdown */}
      <div style={{ marginBottom: 24, display: 'flex', alignItems: 'center', background: '#f3e5f5', borderRadius: 6 }}>
        <span style={{ fontSize: 18, margin: '0 8px' }}>üíÉ</span>
        <select
          value={danceStyle}
          onChange={e => setDanceStyle(e.target.value)}
          style={{ border: 'none', background: 'transparent', outline: 'none', flex: 1, padding: 8, fontSize: 15 }}
        >
          <option value="">Dance Style</option>
          {danceStyles.map(style => (
            <option key={style} value={style}>{style}</option>
          ))}
        </select>
        <span style={{ fontSize: 18, margin: '0 8px' }}>‚ñº</span>
      </div>
      {/* Create Post Button */}
      <button style={{ width: '100%', background: '#9575cd', color: '#fff', border: 'none', borderRadius: 12, padding: '14px 0', fontSize: 20, fontWeight: 600, cursor: 'pointer' }}>
        Create Post
      </button>
    </div>
  );
} 